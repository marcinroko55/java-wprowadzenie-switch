public class calculator {
    public static void main(String[] args) {
        double a = 6.5;
        double b = 7;
        char option = '+';
        switch(option){
            case '+':
                System.out.println(a+b);
                break;
            case '-':
                    System.out.println(a-b);
                    break;
            case '*':
                System.out.println(a*b);
            case '/':
                        System.out.println(a/b);
                        break;




        }
    }
}
